%F             系统模型
%X0            系统初始状态
%t0            飞行时间起点
%tf            飞行时间终点
%N             仿真步数
%U             控制量初始值
%X             初始路径序列
%t             时间序列
%%
%积分求解
%输入模型函数，初始状态，时间起点，时间终点，分段次数，控制变量
%%
function [ t , X ] = jifen( F,X0,t0,tf,N,U )
h = (tf-t0)/N;
X(:,1) = X0;
t(:,1) = t0;
for i = 2:N+1
    t(:,i) = h+t(:,i-1);
    X(:,i) = X(:,i-1)+h*F(X(:,i-1),U(:,i-1)); 
end
end